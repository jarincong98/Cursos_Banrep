function [lhs, rhs] = static_resid(y, x, params)
T = NaN(5, 1);
lhs = NaN(8, 1);
rhs = NaN(8, 1);
T(1) = y(8)^params(4);
T(2) = y(6)^(1-params(4));
T(3) = params(5)*y(6)^params(7);
T(4) = y(3)^params(1);
T(5) = params(9)^(1-params(8));
lhs(1) = y(2);
rhs(1) = y(1)*T(1)*T(2);
lhs(2) = y(7);
rhs(2) = params(4)*y(2)/y(8);
lhs(3) = y(8);
rhs(3) = y(8)*(1-params(3))+y(4);
lhs(4) = y(5);
rhs(4) = (1-params(4))*y(2)/y(6);
lhs(5) = T(3)*T(4);
rhs(5) = y(5);
lhs(6) = 1;
rhs(6) = params(2)*(y(7)+1-params(3));
lhs(7) = y(1);
rhs(7) = y(1)^params(8)*T(5)*(1+x(1));
lhs(8) = y(2);
rhs(8) = y(4)+y(3);
end
