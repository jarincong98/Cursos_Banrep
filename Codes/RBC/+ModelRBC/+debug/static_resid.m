function [lhs, rhs] = static_resid(y, x, params)
T = NaN(10, 1);
lhs = NaN(9, 1);
rhs = NaN(9, 1);
T(1) = y(5)^params(4);
T(2) = y(9)*T(1);
T(3) = y(4)^(1-params(4));
T(4) = (y(2)/params(4))^params(4);
T(5) = 1/y(9)*T(4);
T(6) = (y(1)/(1-params(4)))^(1-params(4));
T(7) = params(5)*y(4)^params(6);
T(8) = y(8)^params(1);
T(9) = y(8)^(-params(1));
T(10) = params(8)^(1-params(7));
lhs(1) = y(6);
rhs(1) = T(2)*T(3);
lhs(2) = y(2);
rhs(2) = params(4)*y(3)*y(6)/y(5);
lhs(3) = y(1);
rhs(3) = (1-params(4))*y(3)*y(6)/y(4);
lhs(4) = y(3);
rhs(4) = T(5)*T(6);
lhs(5) = y(5);
rhs(5) = y(5)*(1-params(3))+y(7);
lhs(6) = T(7)*T(8);
rhs(6) = y(1);
lhs(7) = T(9);
rhs(7) = T(9)*params(2)*(1-params(3)+y(6)*params(4)/y(5));
lhs(8) = y(9);
rhs(8) = y(9)^params(7)*T(10)*(1+x(1));
lhs(9) = y(6);
rhs(9) = y(7)+y(8);
end
