function [lhs, rhs] = static_resid(y, x, params)
T = NaN(6, 1);
lhs = NaN(8, 1);
rhs = NaN(8, 1);
T(1) = y(4)^params(4);
T(2) = y(8)*T(1);
T(3) = y(3)^(1-params(4));
T(4) = params(5)*y(3)^params(7);
T(5) = y(7)^params(1);
T(6) = params(9)^(1-params(8));
lhs(1) = y(5);
rhs(1) = T(2)*T(3);
lhs(2) = y(2);
rhs(2) = params(4)*y(5)/y(4);
lhs(3) = y(4);
rhs(3) = y(4)*(1-params(3))+y(6);
lhs(4) = y(1);
rhs(4) = (1-params(4))*y(5)/y(3);
lhs(5) = T(4)*T(5);
rhs(5) = y(1);
lhs(6) = 1;
rhs(6) = params(2)*(y(2)+1-params(3));
lhs(7) = y(8);
rhs(7) = y(8)^params(8)*T(6)*(1+x(1));
lhs(8) = y(5);
rhs(8) = y(6)+y(7);
end
